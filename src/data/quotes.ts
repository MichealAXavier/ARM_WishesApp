const STORAGE_KEY = "arm_quote_index";

export const getNextQuote = (): Quote => {
  // 1. Get the saved index from localStorage (or default to 0)
  const savedIndex = localStorage.getItem(STORAGE_KEY);
  let index = savedIndex ? parseInt(savedIndex, 10) : 0;

  // 2. Get the quote
  const quote = quotes[index];

  // 3. Calculate next index and save it for the NEXT refresh
  const nextIndex = (index + 1) % quotes.length;
  localStorage.setItem(STORAGE_KEY, nextIndex.toString());

  return quote;
};

